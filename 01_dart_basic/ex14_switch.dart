void main() {
  // switch
  // // int k1이 1이면 카페모카, 2이면 카페라떼, 3이면 아메리카노, 4이면 과일쥬스
  int k1 = 2;
  switch (k1) {
    case 1:
      print("카페모카");
      break;
    case 2:
      print("카페라떼");
      break;
    case 3:
      print("아메리카노");
      break;
    case 4:
      print("과일쥬스");
      break;
  }
}
